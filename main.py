import os
import pandas as pd
import numpy as np


def main():
    # Генерация синтетических данных для производственных рисков на основе реальной статистики
    np.random.seed(42)
    years = list(range(2019, 2026))
    data = {
        "Год": years,
        "Общее_количество_инцидентов": np.random.poisson(15, len(years))
        + 10,  # ~10-25 инцидентов
        "Тяжелые_травмы": np.random.poisson(5, len(years)) + 3,
        "Смертельные_исходы": np.zeros(
            len(years), dtype=int
        ),  # все смертельные исходы = 0
        "Оборудование_неисправности": np.random.poisson(8, len(years)) + 5,
        "Человеческий_фактор": np.random.poisson(6, len(years)) + 4,
        "Экологические_риски": np.random.poisson(2, len(years)) + 1,
    }

    # Корректировка на основе реальных данных: 2020 - рост в Солигорске (оставляем прочие корректировки)
    data["Общее_количество_инцидентов"][1] = 22  # 2020
    data["Тяжелые_травмы"][1] = 7
    # Смертельные_исходы остаются 0 (никаких смертельных исходов)

    df = pd.DataFrame(data)

    # Расчет коэффициентов для статистических моделей (пример: частота на 100 работников, предполагаем 500 работников)
    df["Частота_инцидентов_на_100"] = (
        df["Общее_количество_инцидентов"] / 500 * 100
    ).round(2)
    df["Коэффициент_смертности"] = (df["Смертельные_исходы"] / 500 * 100000).round(
        2
    )  # на 100k

    # Убедиться, что папка output существует
    os.makedirs("output", exist_ok=True)

    out_path = os.path.join("output", "производственные_риски_СИПР.csv")
    df.to_csv(out_path, index=False, encoding="utf-8")
    print(f"CSV файл сохранен: {out_path}")


if __name__ == "__main__":
    main()
